def normalize_host(value):
    value = value.strip().rstrip("/")
    if not value:
        return ""
    if value == "*":
        return value
    if "://" in value:
        return urlparse(value).hostname or ""
    return value.split("/")[0].split(":")[0]


ALLOWED_HOSTS = [
    host
    for host in [
        normalize_host(item)
        for item in os.getenv(
            "DJANGO_ALLOWED_HOSTS",
            "localhost,127.0.0.1",
        ).split(",")
    ]
    if host
]

